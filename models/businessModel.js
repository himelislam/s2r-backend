const mongoose = require("mongoose");

const businessSchema =  new mongoose.Schema({
    businessName: {
        type: String,
        required: true
    },
    name: {
        type: String,
        required: true,
    },
    email: {
        type: String,
        required: true,
    },
    businessEmail: {
        type: String,
        required: true,
    },
    phone: {
        type: String,
        required: true,
    },
    address: {
        type: String,
        required: true,
    }
})

module.exports = mongoose.model("business", businessSchema);